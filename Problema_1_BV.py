#De la tastatură se întroduce numărul de rînd al culorii curcubeului. De afişat denumirea culorii. Convenim că culoarea roşie are numărul de rînd 1.
n=int(input('De introdus un numar de la 1 pana la 7:'))
if n==1:
    print('Culoarea sub nuarul 1 din curcubeu este rosu.')
elif n==2:
    print('Culoarea sub nuarul 1 din curcubeu este oranj.')
elif n==3:
    print('Culoarea sub nuarul 1 din curcubeu este galben.')
elif n==4:
    print('Culoarea sub nuarul 1 din curcubeu este verde.')
elif n==5:
    print('Culoarea sub nuarul 1 din curcubeu este albastru.')
elif n==6:
    print('Culoarea sub nuarul 1 din curcubeu este indigo.')
elif n==7:
    print('Culoarea sub nuarul 1 din curcubeu este violet.')

