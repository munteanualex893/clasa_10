"""
Într-o gospodărie sunt 4 găini. Introduceţi în calculator prin variabilele a, b, c, d
numărul de ouă pe care-l dă fiecare găină într-o zi. Afişaţi câte ouă se obţin într-o
săptămână.
"""
a=int(input("Introduceti numarul de oua pe care le da prima gaina intr-o zi:"))
b=int(input("Introduceti numarul de oua pe care le da a doua gaina intr-o zi:"))
c=int(input("Introduceti numarul de oua pe care le da a treia gaina intr-o zi:"))
d=int(input("Introduceti numarul de oua pe care le da a patra gaina intr-o zi:"))
suma=7*(a+b+c+d)
print("Intr-o saptamana gainele vor da ", suma, ' de oua.')
